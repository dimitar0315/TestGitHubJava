package gitHubJavaConnection;

public class Date {
	
	int sec;
	int min;
	int hour;
	int day;
	int month;
	int year;
	
	
	public int getSec() {
		return sec;
	}
	
	public void setSec(int _sec) {
		sec = _sec;
	}
	
	
	public int getMin() {
		return min;
	}
	
	public void setMin(int _min) {
		min = _min;
	}
	
	
	
	public int getHour() {
		return hour;
	}
	
	public void setHour(int _hour) {
		hour = _hour;
	}
	
	
	
	
	public int getDay() {
		return day;
	}
	
	public void setDay(int _day) {
		day = _day;
	}
	
	
	
	
	public int getMonth() {
		return month;
	}
	
	public void setMonth(int _month) {
		month = _month;
	}
	
	
	
	
	public int getYear() {
		return year;
	}
	
	public void setYear(int _Year) {
		year = _Year;
	}
	
	
	
	
	
	
	
	
	
	
	

}
